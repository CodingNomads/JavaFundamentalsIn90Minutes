public class DataTypesAndVariables {

    public static void main(String[] args) {

        // integer round numbers
        int i = 10;
        long l = 14514615476154l;

        // decimal point numbers
        float f = 134513.456f;

        // twice as big as a float
        double x = 13453.123;

        // single character
        char c = 'c';

        // true or false
        boolean b = true;

        // 8 bits
        byte z = 127;

        // 16 bits
        short s = 256;

        // you can modify variables as you please
        i = i * 10;

        // print out "i" - see 100
        System.out.println("The value of i is: " + i);
    }

}
